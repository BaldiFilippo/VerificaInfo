
public class Libro {

    private String titolo;
    private int pagine;

    public Libro(String titolo, int pagine) throws Exception {
        this.titolo = titolo;
        this.pagine = pagine;
    }

    public String getTitolo() {
        return titolo;
    }

    public int getPagine() {
        return pagine;
    }

    public void setTitolo(String titolo) {
        this.titolo = titolo;
    }

    public void setPagine(int pagine) {
        this.pagine = pagine;
    }




   






    
}
